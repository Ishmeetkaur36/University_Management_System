    void fetchStudentDetails()
    {
        Connection conn = null;
        try {
            conn = ConnectionUtil.getInstance();
        
        String sql="select * from student where id=? ";
        PreparedStatement pst=conn.prepareStatement(sql);
        pst.setInt(1,id);
        ResultSet rs=pst.executeQuery();
         
        if(rs.next())
        {
        String id=rs.getString("id");
        String name=rs.getString("name");
        String course=rs.getString("course");
        String branch=rs.getString("branch");
        
        jTextFieldname.setText(name);
        jTextFieldid.setText(String.valueOf(id));
        jComboBoxcourse.setSelectedItem(course);
        jComboBoxbranch.setSelectedItem(branch);
        }}
         catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(FacultyLeave.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

