    private void jButtonsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsubmitActionPerformed

        try {
            if(jTextFieldname.getText().isEmpty()||jTextFielddays.getText().isEmpty()||jTextFielddate.getText().isEmpty()||jTextFieldreason.getText().isEmpty());
            {
                error.setVisible(true);
                error.setText("Fill out all the fields");
            }
            String name=jTextFieldname.getText();
            String id=jTextFieldid.getText();
            String days=jTextFielddays.getText();
            String date=jTextFielddate.getText();
            String reason=jTextFieldreason.getText();
            String course=(String)jComboBoxbranch.getSelectedItem();
            String branch=(String)jComboBoxcourse.getSelectedItem();
            
            String sql="INSERT INTO studentleave(name,id,course,department,date,days,reason) values(?,?,?,?,?,?,?)";
            
            Connection conn=ConnectionUtil.getInstance();
            PreparedStatement pst=conn.prepareStatement(sql);
            pst.setString(1, name);
            pst.setString(2, id);
            pst.setString(3, branch);
            pst.setString(4, course);
            pst.setString(5, date);
            pst.setString(6, days);
            pst.setString(7, reason);
            int a=pst.executeUpdate();
            if(a>0)
               
                {
                    error.setText("Added Successfully");
                }
                else
                {
                    error.setText("Error incurred");
                }
        } catch (SQLException ex) {
            Logger.getLogger(StudentLeave.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StudentLeave.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonsubmitActionPerformed

