    private void jButtonstatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonstatusActionPerformed
       
        try {
            String id=jTextFieldid.getText();
            String sql="select status from studentleave where id=?";
            Connection conn=ConnectionUtil.getInstance();
            PreparedStatement pst =conn.prepareStatement(sql);
            pst.setString(1,id);
            ResultSet rs=pst.executeQuery();
             if(rs.next())
            {
                if(rs.getString("status") == null)
                {
                    error.setText("Your leave is still under review..");
                }
                else if(rs.getString("status").equalsIgnoreCase("approved"))
                {
                    error.setText("Your leave has been approved.");
                }
                else if(rs.getString("status").equalsIgnoreCase("disapprove"))
                {
                    error.setText("Your leave has been disapproved.");
                }
            }
            else{
                error.setText("You haven't applied for leave yet.");
            }

            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FacultyLeave.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(FacultyLeave.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonstatusActionPerformed

